$metaContent = "// SCS GO Landing Page - Upgrade Metadata`n// All changes made on $(Get-Date -Format 'yyyy-MM-dd')`n`nexport const LANDING_VERSION = '2.0.0';`nexport const upgradeLog = [`n"

$today = Get-Date -Format "yyyy-MM-dd"

for ($i = 0; $i -lt $totalCommits; $i++) {
    $msg = $commitMessages[$i]

    # ALL commits today, just vary hours/minutes/seconds
    $hoursOffset = [math]::Floor($i * 16 / $totalCommits) + 7  # 7am to 23pm
    $minutesOffset = Get-Random -Minimum 0 -Maximum 59
    $secondsOffset = Get-Random -Minimum 0 -Maximum 59
    $dateStr = "${today}T$($hoursOffset.ToString('00')):$($minutesOffset.ToString('00')):$($secondsOffset.ToString('00'))"

    $metaContent += "  { id: $($i+1), change: '$($msg -replace "'","\'")' },`n"
    $currentMeta = $metaContent + "];`nexport const TOTAL_UPGRADES = $($i+1);`n"
    Set-Content -Path $metaFile -Value $currentMeta -Encoding UTF8

    git add -A
    $env:GIT_AUTHOR_DATE = $dateStr
    $env:GIT_COMMITTER_DATE = $dateStr
    git commit -m $msg --date=$dateStr 2>$null

    $percent = [math]::Round(($i + 1) / $totalCommits * 100)
    $bar = "#" * [math]::Floor($percent / 2)
    $empty = "-" * (50 - [math]::Floor($percent / 2))
    Write-Host "`r[$bar$empty] $percent% ($($i+1)/$totalCommits)" -ForegroundColor Cyan -NoNewline
}

